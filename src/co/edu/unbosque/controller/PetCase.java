package co.edu.unbosque.controller;

public class PetCase {
	
	private String type;
	private String specie;
	private String size;
	private String neighbourhood;
	private String address;
	private String reporterName;
	private String reporterTelephone;
	private String reporterEmail;
	private String comments;
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the specie
	 */
	public String getSpecie() {
		return specie;
	}
	/**
	 * @param specie the specie to set
	 */
	public void setSpecie(String specie) {
		this.specie = specie;
	}
	/**
	 * @return the size
	 */
	public String getSize() {
		return size;
	}
	/**
	 * @param size the size to set
	 */
	public void setSize(String size) {
		this.size = size;
	}
	/**
	 * @return the neighbourhood
	 */
	public String getNeighbourhood() {
		return neighbourhood;
	}
	/**
	 * @param neighbourhood the neighbourhood to set
	 */
	public void setNeighbourhood(String neighbourhood) {
		this.neighbourhood = neighbourhood;
	}
	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}
	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}
	/**
	 * @return the reporterName
	 */
	public String getReporterName() {
		return reporterName;
	}
	/**
	 * @param reporterName the reporterName to set
	 */
	public void setReporterName(String reporterName) {
		this.reporterName = reporterName;
	}
	/**
	 * @return the reporterTelephone
	 */
	public String getReporterTelephone() {
		return reporterTelephone;
	}
	/**
	 * @param reporterTelephone the reporterTelephone to set
	 */
	public void setReporterTelephone(String reporterTelephone) {
		this.reporterTelephone = reporterTelephone;
	}
	/**
	 * @return the reporterEmail
	 */
	public String getReporterEmail() {
		return reporterEmail;
	}
	/**
	 * @param reporterEmail the reporterEmail to set
	 */
	public void setReporterEmail(String reporterEmail) {
		this.reporterEmail = reporterEmail;
	}
	/**
	 * @return the comments
	 */
	public String getComments() {
		return comments;
	}
	/**
	 * @param comments the comments to set
	 */
	public void setComments(String comments) {
		this.comments = comments;
	}
	
	
	
	
	

}
